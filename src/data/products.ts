// 根据分类获取商品
export const getProductsByCategory = (category: string) => {
  if (category === 'all') return products;
  return products.filter(p => p.category === category);
};

// 获取积分兑换商品
export const getPointsProducts = () => {
  return products.filter(p => p.points && p.points > 0);
};
